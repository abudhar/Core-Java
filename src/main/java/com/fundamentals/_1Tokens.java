package com.fundamentals;

public class _1Tokens {
 /*
  * Lexemes: Fundamental keyword (+,-,/,*) or (int, for, break)
  * Tokens/ Keyword:  Collection of Lexemes are called Tokens/Keyword
  * 
  * Totally (4 types) of Tokens:
  * 	1.Indentifiers
  * 	2.Literals
  * 	3.Keyword/Reserve Words
  * 	4.Operators 
  */
	public static void main(String[] args) {
		String String = "Hi";
		System.out.println(String);
		String Exception = "ex";
		System.out.println(Exception);
		String System = "hiii";
		java.lang.System.out.println(System);
		System = System + "_ Bye";
		java.lang.System.out.println(System);
	}
}
