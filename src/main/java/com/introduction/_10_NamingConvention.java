package com.introduction;

public class _10_NamingConvention {
	/*
	 * Java is Case Sensitive PL.
	 * 
	 * CAMELCASE:
	 * 		-> Class - (Parent, Children)
	 * 		-> Abstract Classes (Parent, Child)
	 * 		-> Interface
	 * 		-> Enum
	 * 
	 * UPPERCASE:
	 * 		->	constants (MAX_VALUE, MIN_VALUE)
	 * 		
	 * LOWERCASE: (first letter is Lowercase, subsequent First Letter should be Caps)
	 * 		-> Variable - (out, in, inputVarible)
	 * 		-> Method   - (print, printString)
	 * 		
	 * LOWERCASE: 
	 * 		-> Package Name (java.io, java.util)
	 * 		
	 * Predifined Library is Case Sentive (mandatory)
	 * User Define Library can be case sensitive (optional)
	 * 
	 */
}
