public class _08Memory_Allocation {
	
	/*
	 * C/CPP:
	 * 		-int (2 bytes)
	 * 		-char (1 byte)
	 * Java: 
	 * 		-int(4 byte)
	 * 		-char(2 byte)
	 * 
	 * Memory Allocation in C/Cpp for primitive data type is not defined its consider as variable
	 * In Java memory has been Allocated pre-defined,
		 * 	byte - 1 byte
		 * 	short - 2 byte
		 * 	int - 4 byte
		 * 	long - 8 byte
		 * 	float - 4 byte
		 * 	double - 8 byte
		 * 	char - 2 byte
		 * 	boolean - 1 bit
	 * 
	 * Java is taking More memory than C/CPP in Char because,
	 * 
	 * 		C/CPP -> Charecters Using ASCII
	 * 		Java  -> UNICODE (Internationalization Support) [I18N]
	 * 
	 * 	Internaltionalization: Designing application on Local Language Convention.
	 * 
	 * 	Ex: Google : eng,tamil,telegu hyperlinks
	 */

}
